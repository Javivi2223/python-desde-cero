#es posible crear un rango de numeros sucesivos utilizando la funcion range() e iterarla usando for

#con un solo argumento, range parte desde el cero hasta el entero onferior
for num in range(6):
    print(num)

print("---------------")

#con dos argumentos entrega un listado a partir del primer numero (incluido) hasta el entero inferior
for num in range(1,6):
    print(num)